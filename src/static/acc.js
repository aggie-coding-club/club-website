function handleFormerOfficers() {
  const formerOfficers = document.getElementById('former-officers');

  if (formerOfficers.style.display === 'none') {
    formerOfficers.style.display = 'inline';
  } else {
    formerOfficers.style.display = 'none';
  }
}
